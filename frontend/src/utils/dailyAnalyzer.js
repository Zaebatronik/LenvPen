/**
 * –ë–õ–û–ö D2 - –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –∞–Ω–∞–ª–∏–∑ –¥–µ–π—Å—Ç–≤–∏–π
 * –ò–ò-–∞–Ω–∞–ª–∏–∑–∞—Ç–æ—Ä –µ–∂–µ–¥–Ω–µ–≤–Ω—ã—Ö –æ—Ç—á—ë—Ç–æ–≤ —Å —é–º–æ—Ä–æ–º –∏ –∏–Ω—Ç–µ–ª–ª–µ–∫—Ç–æ–º
 */

/**
 * –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç –¥–Ω–µ–≤–Ω–æ–π –æ—Ç—á—ë—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
 * @param {Object} reportData - –¥–∞–Ω–Ω—ã–µ –æ—Ç—á—ë—Ç–∞
 * @returns {Object} —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∞–Ω–∞–ª–∏–∑–∞ —Å –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏—è–º–∏ –∏ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏—è–º–∏
 */
export function analyzeDailyReport(reportData) {
  const { dependencies, usefulActions, mood, comment } = reportData;
  
  const analysis = {
    overall: 'neutral', // good | neutral | bad
    comment: '',
    improvements: [],
    concerns: [],
    recommendations: [],
    dependencyChanges: {}, // { depName: { change: +/-, newLevel: number } }
    goalImpact: 0, // -10 to +10
    slothMood: 'neutral' // happy | neutral | concerned | sad
  };
  
  // –ü–æ–¥—Å—á—ë—Ç –ø–æ–ª–µ–∑–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π
  const usefulCount = Object.values(usefulActions).filter(Boolean).length;
  
  // –ü–æ–¥—Å—á—ë—Ç —Å—Ä—ã–≤–æ–≤ –∏ –ø–æ–±–µ–¥ –ø–æ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç—è–º
  let resistedCount = 0;
  let exceededCount = 0;
  
  Object.entries(dependencies).forEach(([depName, data]) => {
    if (data.resisted) resistedCount++;
    if (data.exceeded) exceededCount++;
  });
  
  // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –æ–±—â—É—é –æ—Ü–µ–Ω–∫—É –¥–Ω—è
  const totalScore = (usefulCount * 2) + (resistedCount * 3) - (exceededCount * 4);
  
  if (totalScore >= 8) {
    analysis.overall = 'good';
    analysis.slothMood = 'happy';
  } else if (totalScore <= -5) {
    analysis.overall = 'bad';
    analysis.slothMood = 'concerned';
  } else {
    analysis.overall = 'neutral';
    analysis.slothMood = 'neutral';
  }
  
  // ‚ïê‚ïê‚ïê –ì–ï–ù–ï–†–ê–¶–ò–Ø –ö–û–ú–ú–ï–ù–¢–ê–†–ò–Ø –° –Æ–ú–û–†–û–ú ‚ïê‚ïê‚ïê
  
  if (analysis.overall === 'good') {
    const goodComments = [
      '–õ–µ–Ω–∏–≤–µ—Ü —Ç–∏—Ö–æ —Ö—Ä—é–∫–Ω—É–ª –æ—Ç —Å—á–∞—Å—Ç—å—è‚Ä¶ —Ç—ã –º–æ–ª–æ–¥–µ—Ü! ü¶•‚ú®',
      '–õ–µ–Ω–∏–≤–µ—Ü —Ä–∞–∑–æ–≥–Ω—É–ª—Å—è! –≠—Ç–æ —Ä–µ–¥–∫–æ–µ –∑—Ä–µ–ª–∏—â–µ. –û–Ω –≥–æ—Ä–¥–∏—Ç—Å—è —Ç–æ–±–æ–π! üéâ',
      '–°–µ–≥–æ–¥–Ω—è –ª–µ–Ω–∏–≤–µ—Ü —Å–∫–∞–∑–∞–ª —Å–ª–æ–≤–æ ¬´–ú–æ–ª–æ–¥–µ—Ü¬ª. –≠—Ç–æ –∏—Å—Ç–æ—Ä–∏—á–µ—Å–∫–∏–π –º–æ–º–µ–Ω—Ç. üìú',
      '–õ–µ–Ω–∏–≤–µ—Ü –ø–æ–ª–∑—ë—Ç –∫ –º–µ—á—Ç–µ. –ò —Ç—ã –≤–º–µ—Å—Ç–µ —Å –Ω–∏–º. –û—Ç–ª–∏—á–Ω–∞—è —Ä–∞–±–æ—Ç–∞! üåü',
      '–ï—Å–ª–∏ –±—ã –ª–µ–Ω–∏–≤—Ü—ã –±—ã–ª–∏ –ø—Ä–µ–∑–∏–¥–µ–Ω—Ç–∞–º–∏, —Ç—ã –±—ã–ª –±—ã –º–∏–Ω–∏—Å—Ç—Ä–æ–º —Å–∞–º–æ–∫–æ–Ω—Ç—Ä–æ–ª—è. üèÜ',
      '–¢—ã —Å–µ–π—á–∞—Å —Å–¥–µ–ª–∞–ª –±–æ–ª—å—à–µ, —á–µ–º —Ç–≤–æ–π —Ö–æ–ª–æ–¥–∏–ª—å–Ω–∏–∫ –∑–∞ –≤—Å—é –Ω–µ–¥–µ–ª—é. üí™',
      '–õ–µ–Ω–∏–≤–µ—Ü –∑–∞–ø–∏—Å–∞–ª —Ç–µ–±—è –≤ —Å–ø–∏—Å–æ–∫ ¬´–ø–æ—á—Ç–∏ –≥–µ—Ä–æ–µ–≤¬ª. –•–æ—Ä–æ—à–∞—è –¥–∏–Ω–∞–º–∏–∫–∞! üìà'
    ];
    analysis.comment = goodComments[Math.floor(Math.random() * goodComments.length)];
  } else if (analysis.overall === 'bad') {
    const badComments = [
      '–õ–µ–Ω–∏–≤–µ—Ü –ø–æ—Å–º–æ—Ç—Ä–µ–ª –Ω–∞ —Ç–µ–±—è –∫–∞–∫ —Ç—ë—â–∞ –Ω–∞ –ù–æ–≤—ã–π –≥–æ–¥‚Ä¶ –Ω–æ –≤–µ—Ä–∏—Ç, —á—Ç–æ —Ç—ã —Å–ø—Ä–∞–≤–∏—à—å—Å—è. üëÄ',
      '–õ–µ–Ω–∏–≤–µ—Ü –º–æ–ª—á–∞ –ª—ë–≥. –û–Ω –Ω–µ –æ—Å—É–∂–¥–∞–µ—Ç. –û–Ω –ø–æ–Ω–∏–º–∞–µ—Ç. ü´Ç',
      '–û–ø—è—Ç—å? –û–Ω —É–∂–µ –∑–∞–≤–∞—Ä–∏–ª —á–∞–π –∏ –ø—Ä–∏–≥–æ—Ç–æ–≤–∏–ª –ø–ª–µ–¥. –ù–æ –∑–∞–≤—Ç—Ä–∞ ‚Äî –Ω–æ–≤—ã–π –¥–µ–Ω—å! ‚òï',
      '–õ–µ–Ω–∏–≤–µ—Ü –≤–∑–¥–æ—Ö–Ω—É–ª —Ç–∞–∫ —Ç—è–∂–µ–ª–æ, —á—Ç–æ –ª–∏—Å—Ç—å—è –Ω–∞ –¥–µ—Ä–µ–≤–µ –∑–∞–¥—Ä–æ–∂–∞–ª–∏. –ù–æ –æ–Ω —Å —Ç–æ–±–æ–π. üçÉ',
      '–°–µ–≥–æ–¥–Ω—è –±—ã–ª –Ω–µ –ª—É—á—à–∏–π –¥–µ–Ω—å, –Ω–æ –ª–µ–Ω–∏–≤–µ—Ü –≤–∏–¥–µ–ª –∏ –ø–æ—Ö—É–∂–µ. –î–µ—Ä–∂–∏—Å—å! üí™',
      '–õ–µ–Ω–∏–≤–µ—Ü –¥–æ—Å—Ç–∞–ª —Å–≤–æ–π –±–ª–æ–∫–Ω–æ—Ç "–î–Ω–∏, –∫–æ—Ç–æ—Ä—ã–µ –ª—É—á—à–µ –∑–∞–±—ã—Ç—å"‚Ä¶ –Ω–æ –∑–∞–≤—Ç—Ä–∞ –≤—Å—ë –∏–∑–º–µ–Ω–∏—Ç—Å—è! üìì',
      '–î–∞–∂–µ –ª–µ–Ω–∏–≤—Ü—ã –∏–Ω–æ–≥–¥–∞ –ø–∞–¥–∞—é—Ç —Å –¥–µ—Ä–µ–≤–∞. –ì–ª–∞–≤–Ω–æ–µ ‚Äî —Å–Ω–æ–≤–∞ –∑–∞–ª–µ–∑—Ç—å. üå≥'
    ];
    analysis.comment = badComments[Math.floor(Math.random() * badComments.length)];
  } else {
    const neutralComments = [
      '–õ–µ–Ω–∏–≤–µ—Ü –∫–∏–≤–Ω—É–ª. –î–µ–Ω—å –∫–∞–∫ –¥–µ–Ω—å. –ù–æ –ø–æ—Ç–µ–Ω—Ü–∏–∞–ª –µ—Å—Ç—å! üëå',
      '–õ–µ–Ω–∏–≤–µ—Ü —Å–∫–∞–∑–∞–ª: "–ù–µ–ø–ª–æ—Ö–æ, –Ω–æ —è –≤–∏–¥–µ–ª –ª—É—á—à–µ‚Ä¶ —É —Ç–µ–±—è —Å–∞–º–æ–≥–æ". ü§î',
      '–û–±—ã—á–Ω—ã–π –¥–µ–Ω—å. –õ–µ–Ω–∏–≤–µ—Ü –∂–¥—ë—Ç —Ç–≤–æ–µ–≥–æ –ø—Ä–æ—Ä—ã–≤–∞. üìä',
      '–õ–µ–Ω–∏–≤–µ—Ü –ø–æ–∂–∞–ª –ø–ª–µ—á–∞–º–∏‚Ä¶ –≤ —Å–º—ã—Å–ª–µ, –æ–Ω –ø–æ–ø—ã—Ç–∞–ª—Å—è. –î–µ–Ω—å —Å—Ä–µ–¥–Ω–∏–π. üòê',
      '–ù–∏ –≤–≤–µ—Ä—Ö, –Ω–∏ –≤–Ω–∏–∑. –õ–µ–Ω–∏–≤–µ—Ü –∑–æ–≤—ë—Ç —ç—Ç–æ "–ª–µ–Ω–∏–≤–æ–µ —Ä–∞–≤–Ω–æ–≤–µ—Å–∏–µ". ‚öñÔ∏è',
      '–î–µ–Ω—å –±—ã–ª‚Ä¶ –∫–∞–∫ –ø–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫, –Ω–æ –Ω–µ –≤ –ø–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫. –õ–µ–Ω–∏–≤–µ—Ü –ø–æ–Ω–∏–º–∞–µ—Ç. üìÖ'
    ];
    analysis.comment = neutralComments[Math.floor(Math.random() * neutralComments.length)];
  }
  
  // ‚ïê‚ïê‚ïê –£–õ–£–ß–®–ï–ù–ò–Ø ‚ïê‚ïê‚ïê
  
  if (resistedCount > 0) {
    analysis.improvements.push(`–£–¥–µ—Ä–∂–∞–ª—Å—è –æ—Ç ${resistedCount} –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç${resistedCount === 1 ? '–∏' : '–µ–π'} ‚Äî —Å–∏–ª–∞ –≤–æ–ª–∏ —Ä–∞—Å—Ç—ë—Ç!`);
  }
  
  if (usefulCount >= 4) {
    analysis.improvements.push(`${usefulCount} –ø–æ–ª–µ–∑–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π ‚Äî —Ç—ã –Ω–∞ –æ–≥–Ω–µ! üî•`);
  } else if (usefulCount >= 2) {
    analysis.improvements.push(`${usefulCount} –ø–æ–ª–µ–∑–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏—è ‚Äî —Ö–æ—Ä–æ—à–∏–π —Å—Ç–∞—Ä—Ç!`);
  }
  
  if (mood.mood >= 7) {
    analysis.improvements.push('–ù–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ –Ω–∞ –≤—ã—Å–æ—Ç–µ ‚Äî —ç—Ç–æ –∑–∞—Ä–∞–∑–∏—Ç–µ–ª—å–Ω–æ!');
  }
  
  if (mood.sleep >= 7) {
    analysis.improvements.push('–•–æ—Ä–æ—à–∏–π —Å–æ–Ω ‚Äî –æ—Å–Ω–æ–≤–∞ –ø—Ä–æ–¥—É–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏!');
  }
  
  if (mood.stress <= 3) {
    analysis.improvements.push('–ù–∏–∑–∫–∏–π —Å—Ç—Ä–µ—Å—Å ‚Äî —Ç—ã –Ω–∞—à—ë–ª –±–∞–ª–∞–Ω—Å!');
  }
  
  // ‚ïê‚ïê‚ïê –ü–†–û–ë–õ–ï–ú–´ ‚ïê‚ïê‚ïê
  
  if (exceededCount > 0) {
    analysis.concerns.push(`${exceededCount} –ø—Ä–µ–≤—ã—à–µ–Ω–∏${exceededCount === 1 ? '–µ' : '–π'} –ª–∏–º–∏—Ç–∞ ‚Äî –≤–Ω–∏–º–∞–Ω–∏–µ –∫ —Ç—Ä–∏–≥–≥–µ—Ä–∞–º!`);
  }
  
  if (usefulCount === 0) {
    analysis.concerns.push('–ù–∏ –æ–¥–Ω–æ–≥–æ –ø–æ–ª–µ–∑–Ω–æ–≥–æ –¥–µ–π—Å—Ç–≤–∏—è ‚Äî –æ—Ä–≥–∞–Ω–∏–∑–º—É –Ω—É–∂–Ω–∞ –ø–æ–¥–¥–µ—Ä–∂–∫–∞');
  }
  
  if (mood.mood <= 3) {
    analysis.concerns.push('–ù–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ –Ω–∏–∑–∫–æ–µ ‚Äî –≤–æ–∑–º–æ–∂–Ω–æ, –Ω—É–∂–Ω–∞ –ø–∞—É–∑–∞ –∏–ª–∏ –ø–æ–º–æ—â—å');
  }
  
  if (mood.sleep < 6) {
    analysis.concerns.push('–ù–µ–¥–æ—Å—Ç–∞—Ç–æ–∫ —Å–Ω–∞ –≤–ª–∏—è–µ—Ç –Ω–∞ —Å–∞–º–æ–∫–æ–Ω—Ç—Ä–æ–ª—å ‚Äî –≤—ã—Å—ã–ø–∞–π—Å—è!');
  }
  
  if (mood.stress >= 8) {
    analysis.concerns.push('–í—ã—Å–æ–∫–∏–π —Å—Ç—Ä–µ—Å—Å ‚Äî –∏—â–∏ —Å–ø–æ—Å–æ–±—ã —Ä–∞—Å—Å–ª–∞–±–∏—Ç—å—Å—è');
  }
  
  if (mood.energy <= 3) {
    analysis.concerns.push('–ù–∏–∑–∫–∞—è —ç–Ω–µ—Ä–≥–∏—è ‚Äî –≤–æ–∑–º–æ–∂–Ω–æ, –Ω—É–∂–Ω—ã –≤–∏—Ç–∞–º–∏–Ω—ã –∏–ª–∏ –¥–≤–∏–∂–µ–Ω–∏–µ');
  }
  
  // ‚ïê‚ïê‚ïê –†–ï–ö–û–ú–ï–ù–î–ê–¶–ò–ò –ù–ê –ó–ê–í–¢–†–ê ‚ïê‚ïê‚ïê
  
  if (exceededCount > 0) {
    analysis.recommendations.push('–û–ø—Ä–µ–¥–µ–ª–∏ –≥–ª–∞–≤–Ω—ã–π —Ç—Ä–∏–≥–≥–µ—Ä —Å—Ä—ã–≤–∞ –∏ –ø–æ–¥–≥–æ—Ç–æ–≤—å —Å—Ç—Ä–∞—Ç–µ–≥–∏—é –∏–∑–±–µ–≥–∞–Ω–∏—è');
  }
  
  if (usefulCount < 3) {
    analysis.recommendations.push('–ü–æ–ø—Ä–æ–±—É–π –¥–æ–±–∞–≤–∏—Ç—å —Ö–æ—Ç—è –±—ã 2 –ø–æ–ª–µ–∑–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏—è –∑–∞–≤—Ç—Ä–∞');
  }
  
  if (mood.sleep < 7) {
    analysis.recommendations.push('–ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç –Ω–∞ —Å–æ–Ω: 7-8 —á–∞—Å–æ–≤ –∫—Ä–∏—Ç–∏—á–µ—Å–∫–∏ –≤–∞–∂–Ω—ã');
  }
  
  if (mood.stress >= 7) {
    analysis.recommendations.push('–í–∫–ª—é—á–∏ —Ç–µ—Ö–Ω–∏–∫—É –¥—ã—Ö–∞–Ω–∏—è 4-7-8 –∏–ª–∏ 10-–º–∏–Ω—É—Ç–Ω—É—é –º–µ–¥–∏—Ç–∞—Ü–∏—é');
  }
  
  if (!usefulActions.walk) {
    analysis.recommendations.push('–ü—Ä–æ–≥—É–ª–∫–∞ –Ω–∞ —Å–≤–µ–∂–µ–º –≤–æ–∑–¥—É—Ö–µ ‚Äî –ø—Ä–æ—Å—Ç–æ–π —Å–ø–æ—Å–æ–± —É–ª—É—á—à–∏—Ç—å –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ');
  }
  
  if (!usefulActions.water) {
    analysis.recommendations.push('2+ –ª–∏—Ç—Ä–∞ –≤–æ–¥—ã –ø–æ–º–æ–≥—É—Ç —Ñ–æ–∫—É—Å—É –∏ —ç–Ω–µ—Ä–≥–∏–∏');
  }
  
  // –î–æ–±–∞–≤–ª—è–µ–º —é–º–æ—Ä–∏—Å—Ç–∏—á–µ—Å–∫–∏–µ —Å–ª—É—á–∞–π–Ω—ã–µ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏
  if (Math.random() < 0.3) {
    const funRecommendations = [
      '–ü–æ–ø—Ä–æ–±—É–π —É–ª—ã–±–Ω—É—Ç—å—Å—è —Å–≤–æ–µ–º—É –æ—Ç—Ä–∞–∂–µ–Ω–∏—é. –°–µ—Ä—å—ë–∑–Ω–æ, —ç—Ç–æ —Ä–∞–±–æ—Ç–∞–µ—Ç.',
      '–û–±–Ω–∏–º–∏ –¥–µ—Ä–µ–≤–æ. –ò–ª–∏ –∫–æ—Ç–∞. –ò–ª–∏ —Ö–æ–ª–æ–¥–∏–ª—å–Ω–∏–∫. –ì–ª–∞–≤–Ω–æ–µ ‚Äî –æ–±–Ω–∏–º–∏ –∫–æ–≥–æ-—Ç–æ.',
      '–¢–∞–Ω—Ü—É–π –∫–∞–∫ –±—É–¥—Ç–æ –Ω–∏–∫—Ç–æ –Ω–µ –≤–∏–¥–∏—Ç. –î–∞–∂–µ –µ—Å–ª–∏ –≤–∏–¥—è—Ç ‚Äî –ø—É—Å—Ç—å –∑–∞–≤–∏–¥—É—é—Ç.',
      '–°—ä–µ—à—å —á—Ç–æ-—Ç–æ –≤–∫—É—Å–Ω–æ–µ, –Ω–æ –±–µ–∑ —á—É–≤—Å—Ç–≤–∞ –≤–∏–Ω—ã. –≠—Ç–æ –∑–∞–∫–æ–Ω–Ω–æ.',
      '–í–∫–ª—é—á–∏ –ª—é–±–∏–º—É—é –ø–µ—Å–Ω—é –∏ –ø–æ–¥–ø–µ–≤–∞–π –≥—Ä–æ–º–∫–æ. –°–æ—Å–µ–¥–∏ –ø–µ—Ä–µ–∂–∏–≤—É—Ç.'
    ];
    analysis.recommendations.push(funRecommendations[Math.floor(Math.random() * funRecommendations.length)]);
  }
  
  // ‚ïê‚ïê‚ïê –í–õ–ò–Ø–ù–ò–ï –ù–ê –ì–õ–ê–í–ù–£–Æ –¶–ï–õ–¨ ‚ïê‚ïê‚ïê
  
  analysis.goalImpact = Math.min(10, Math.max(-10, totalScore));
  
  return analysis;
}

/**
 * –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ—Ç –∏–∑–º–µ–Ω–µ–Ω–∏–µ —É—Ä–æ–≤–Ω—è –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏
 * D3 - –î–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–µ –≤–ª–∏—è–Ω–∏–µ –Ω–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏
 */
export function calculateDependencyChange(depName, reportData) {
  const depReport = reportData.dependencies[depName];
  if (!depReport) return { change: 0, newLevel: null };
  
  let change = 0;
  
  // –ï—Å–ª–∏ —É–¥–µ—Ä–∂–∞–ª—Å—è ‚Äî —É—Ä–æ–≤–µ–Ω—å —Å–Ω–∏–∂–∞–µ—Ç—Å—è
  if (depReport.resisted) {
    change = -5; // -5% –∫ —É—Ä–æ–≤–Ω—é –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏
  }
  
  // –ï—Å–ª–∏ –±—ã–ª–æ –ø—Ä–µ–≤—ã—à–µ–Ω–∏–µ ‚Äî —É—Ä–æ–≤–µ–Ω—å —Ä–∞—Å—Ç—ë—Ç
  if (depReport.exceeded) {
    change = +3; // +3% –∫ —É—Ä–æ–≤–Ω—é –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏
    
    // –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π —à—Ç—Ä–∞—Ñ –∑–∞ —Å–µ—Ä—å—ë–∑–Ω—ã–µ –ø—Ä–∏—á–∏–Ω—ã
    if (depReport.reason === 'stress') {
      change += 1;
    }
  }
  
  // –ï—Å–ª–∏ –µ—Å—Ç—å –ø–ª–∞–Ω –ø—Ä–æ—Ñ–∏–ª–∞–∫—Ç–∏–∫–∏ ‚Äî –±–æ–Ω—É—Å
  if (depReport.prevention && depReport.prevention.length > 10) {
    change -= 1; // –æ—Å–æ–∑–Ω–∞–Ω–Ω–æ—Å—Ç—å —Å–Ω–∏–∂–∞–µ—Ç —É—Ä–æ–≤–µ–Ω—å
  }
  
  return {
    change,
    impactReason: depReport.resisted ? '–°–æ–ø—Ä–æ—Ç–∏–≤–ª–µ–Ω–∏–µ' : 
                  depReport.exceeded ? '–ü—Ä–µ–≤—ã—à–µ–Ω–∏–µ' : '–ë–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π'
  };
}

/**
 * –û–±–Ω–æ–≤–ª—è–µ—Ç –ø—Ä–æ–≥—Ä–µ—Å—Å –≥–ª–∞–≤–Ω–æ–π —Ü–µ–ª–∏ –Ω–∞ –æ—Å–Ω–æ–≤–µ –æ—Ç—á—ë—Ç–∞
 * D4 - –í–ª–∏—è–Ω–∏–µ –Ω–∞ –≥–ª–∞–≤–Ω—É—é —Ü–µ–ª—å
 */
export function calculateGoalImpact(reportData, currentGoalProgress) {
  const { dependencies, usefulActions, mood } = reportData;
  
  let impact = 0;
  
  // –ü–æ–ª–µ–∑–Ω—ã–µ –¥–µ–π—Å—Ç–≤–∏—è –¥–≤–∏–≥–∞—é—Ç –∫ —Ü–µ–ª–∏
  const usefulCount = Object.values(usefulActions).filter(Boolean).length;
  impact += usefulCount * 0.5; // –∫–∞–∂–¥–æ–µ –¥–µ–π—Å—Ç–≤–∏–µ = +0.5%
  
  // –£–¥–µ—Ä–∂–∞–Ω–∏–µ –æ—Ç –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π = –ø—Ä–æ–≥—Ä–µ—Å—Å
  Object.values(dependencies).forEach(dep => {
    if (dep.resisted) impact += 1;
    if (dep.exceeded) impact -= 1.5;
  });
  
  // –ù–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ –≤–ª–∏—è–µ—Ç –Ω–∞ –º–æ—Ç–∏–≤–∞—Ü–∏—é
  if (mood.mood >= 7) impact += 0.5;
  if (mood.mood <= 3) impact -= 0.5;
  
  // –ù–æ–≤—ã–π –ø—Ä–æ–≥—Ä–µ—Å—Å (0-100%)
  const newProgress = Math.min(100, Math.max(0, currentGoalProgress + impact));
  
  return {
    oldProgress: currentGoalProgress,
    newProgress,
    change: impact,
    message: impact > 0 ? `+${impact.toFixed(1)}% –∫ —Ü–µ–ª–∏!` : 
             impact < 0 ? `${impact.toFixed(1)}% –æ—Ç —Ü–µ–ª–∏` : 
             '–ë–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π'
  };
}

export default {
  analyzeDailyReport,
  calculateDependencyChange,
  calculateGoalImpact
};
